# Temperature COnversion
print ("\n Temperature Conversion from Celcius\n")

celcius = float(input('input celcius temperature = '))
#print ('temperature in celcius is = ',celcius)

reamur = (4/5) * celcius #convert celcius to reamur
fahrenheit = ((9/5) * celcius) + 32 #convert celcius to fahrenheit
kelvin = celcius + 273.15 #convert celcius to kelvin


print (celcius,'celcius = ',reamur,'reamur')
print (celcius,'celcius = ',fahrenheit,'fahrenheit')
print (celcius,'celcius = ',kelvin,'kelvin')








